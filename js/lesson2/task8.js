let grade = Number(prompt("number for grade"));
switch(grade){
    case 1:
        console.log("Плохо");
    break;
    case 2:
        console.log("неудовлетворительно");
    break;
    case 3:
        console.log("удовлетворительно");
    break;
    case 4:
        console.log("хорошо");
    break;  
    case 5:
        console.log("отлично");
    break;
    default:
        console.log("Ошибка")           
}